class operation {
  plus(String x, String y) {
    return double.parse(x) + double.parse(y);
  }

  subtraction(String x, String y) {
    return double.parse(x) - double.parse(y);
  }

  multiplication(String x, String y) {
    return double.parse(x) * double.parse(y);
  }

  division(String x, String y) {
    return double.parse(x) / double.parse(y);
  }
}
